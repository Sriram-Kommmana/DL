//------------- FUNCTIONS -------------//
string permute(string in,int table[],int n)
{
    string out="";
    for(int i=0;i<n;i++)
        out+=in[table[i]-1];
    return out;
}

string XOR(string a,string b)
{
    string r="";
    for(int i=0;i<a.size();i++)
        r+=(a[i]==b[i])?'0':'1';
    return r;
}

string decToBin(int x)
{
    string r="";
    while(x){r=char(x%2+'0')+r;x/=2;}
    while(r.length()<4)r='0'+r;
    return r;
}

string SBOX(string in)
{
    string out="";
    for(int i=0;i<48;i+=6)
    {
        string b=in.substr(i,6);
        int row=(b[0]-'0')*2+(b[5]-'0');
        int col=(b[1]-'0')*8+(b[2]-'0')*4+(b[3]-'0')*2+(b[4]-'0');
        out+=decToBin(S1[row][col]);
    }
    return out;
}

string F(string R,string K)
{
    cout<<"\nExpanded R = "<<permute(R,E,48)<<endl;
    string ER=permute(R,E,48);
    string x=XOR(ER,K);
    cout<<"After XOR = "<<x<<endl;
    string s=SBOX(x);
    cout<<"After SBOX = "<<s<<endl;
    return s;
}

//----------- CRYPTANALYSIS -----------//
void freq(string txt)
{
    int f[2]={0};
    for(char c:txt) f[c-'0']++;

    cout<<"\nRelative Frequency:\n";
    for(int i=0;i<2;i++)
        cout<<i<<" -> "<<(float)f[i]/txt.length()<<endl;
}

